// falsy values => 0, "", NaN, undefined, false, null
// truth values => todos os demais
if (undefined) {
    console.log("true")
} else {
    console.log("false")
}
if (null) {
    console.log("true")
} else {
    console.log("false")
}
if ("oi") {
    console.log("true")
} else {
    console.log("false")
}
if ("oi" * 2) {
    console.log("true")
} else {
    console.log("false")
}